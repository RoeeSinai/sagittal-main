const computeCentsFromMonzo = <T extends NumericTypeParameters>(
    monzo: PotentiallyNonintegerMonzoParameter<T>,
): Cents => {
    if (monzo.length > PRIME_FACTOR_CENTS.length) {
        const ratio = computeRatioFromMonzo(monzo)

        return computeCentsFromRatio(ratio)
    }

    return monzo.reduce(
        (totalCents: Cents, term: Exponent<Prime>, index: number): Cents => {
            return totalCents + term * PRIME_FACTOR_CENTS[ index ] as Cents
        },
        0 as Cents,
    )
}

